/*    
        Q. Write a program to print natural numbers from 10 to 20 when initial loop 
         counter is initialized to 0.                                                               
*/
#include <stdio.h>

int main(){
    int i=10;
    while (10<=i&&i<=20)
    {
        printf("%d \n",i);
        i++;
    }
    
    return 0;
}